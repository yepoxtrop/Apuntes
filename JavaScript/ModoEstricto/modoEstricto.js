"use strict"

// En modo estricto, no se pueden usar variables sin declararlas
// Esto ayuda a evitar errores comunes y mejora la seguridad del código

let x = 10; // Esto es correcto
console.log(x); // Imprime 10

y = 2; // Esto generará un error porque 'y' no está declarada
console.log(y); // No se ejecutará debido al error anterior