//Funcion normal
function mensaje() {
    console.log("Hola mundo");
}
mensaje();

//Funcion anónima
const mensajeAnonimo = function() {
    console.log("Hola mundo desde la función anónima");
}
mensajeAnonimo();

//Funcion flecha
const mensajeFlecha = () => {
    console.log("Hola mundo desde la función flecha");
}
mensajeFlecha();